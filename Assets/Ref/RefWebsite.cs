//---

//### ✅ **1. [Kenney.nl](https://kenney.nl/assets)**

//* **特点**：Kenney 提供大量高质量的 low poly 资产包，专为游戏开发设计。
//* **授权**：CC0（完全免费，可用于商业项目，无需署名）
//* **推荐理由**：广受开发者喜爱，风格统一，特别适合原型开发或小型游戏。

//---

//### ✅ **2. [Poly Pizza](https://www.polypizza.com/)**

//* **特点**：专注于 Low Poly 3D 模型，有多种风格（卡通、简约等）
//* **授权**：大部分模型为 CC0 或可商用许可（查看每个模型的授权信息）
//* **推荐理由**：界面现代，搜索方便，适合快速查找资源。

//---

//### ✅ **3. [Sketchfab](https://sketchfab.com/)**

//* **路径**：浏览 → 下载 → 筛选“Downloadable” + “Low Poly” + 许可证为“可用于商业用途”
//* **授权**：需手动筛选，部分模型是 CC BY 或 CC0，有些需要署名
//* **推荐理由**：模型种类多，搜索强大，但要注意授权细节。

//---

//### ✅ **4. [Quaternius](https://quaternius.com/)**

//* **特点**：全部模型为免费、可商用、免署名，涵盖人物、怪物、建筑等
//* **授权**：完全免费，可用于商业项目，无需署名
//* **推荐理由**：Low Poly 风格非常一致，适合开发完整的游戏项目。

//---

//### ✅ **5. [CGTrader – Free 3D Models](https://www.cgtrader.com/free-3d-models)**

//* **路径**：搜索“low poly” + 筛选“Free”
//* **授权**：大部分支持商业使用，但需确认每个模型的 License（Standard 或 Editorial Use）
//* **推荐理由**：资源丰富，适合找特定主题模型。

//---

//### ✅ **6. [OpenGameArt.org](https://opengameart.org/)**

//* **路径**：搜索“low poly” + 筛选3D模型
//* **授权**：多样，通常标明为 CC0、CC-BY、GPL 等（查看授权要求）
//* **推荐理由**：社区驱动的游戏资源网站，适合开发者使用。

//---

//### ⚠️ 使用建议：

//* **务必确认每个模型的授权协议**，特别是是否允许商业用途、是否需要署名。
//* 如果使用 CC-BY 模型，记得在游戏中或项目说明中添加作者署名信息。

